public class Main
{
	public static void main(String[] args) {

    int valor1 = 200;
    int valor2 = 60;
    
    //Operadores Aritméticos
    int resultado1 = valor1 + valor2;
    System.out.println("A Soma do Valor1 e do Valor2: " + resultado1);
    
    int resultado2 = valor1 - valor2;
    System.out.println("A Subtração do Valor1 e do Valor2: " + resultado2);
    
    int resultado3 = valor1 * valor2;
    System.out.println("A Multiplicação do Valor1 e do Valor2: " + resultado3);
    
    int resultado4 = valor1 / valor2;
    System.out.println("A Divisao do Valor1 e do Valor2: " + resultado4);
    
    int resultado5 = valor1 % valor2;
    System.out.println("O Resto da Divisao Valor1 e do Valor2: " + resultado5);
	}
}
